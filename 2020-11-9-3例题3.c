#include<stdio.h>
int main()
{
	char a = 128;//127+1，就变成-128
	printf("%u\n",a);//此处为%u（打印十进制无符号数）
	//00000000 00000000 00000000 10000000（-128原码）
	//01111111 11111111 11111111 01111111（-128反码）
	//01111111 11111111 11111111 10000000（-128的补码）
	//11111111 11111111111111111 10000000（整形提升，为有符号数，空位补1），此时此数为%u无符号数，故原码和反码一样
	//11111111 11111111111111111 10000000（原码和补码一样）
	return 0;
}